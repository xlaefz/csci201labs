package client;

public class WorkerTableData {

	private String productName;
	private String workerName;
	private String startedString;
	private String completedString;
	
	public String getProductName() {
		return productName;
	}
	
	public void setProductName(String productName) {
		this.productName = productName;
	}
	
	public String getWorkerName() {
		return workerName;
	}
	
	public void setWorkerName(String workerName) {
		this.workerName = workerName;
	}
	
	public String getStartedString() {
		return startedString;
	}
	
	public void setStartedString(String startedString) {
		this.startedString = startedString;
	}
	
	public String getCompletedString() {
		return completedString;
	}
	
	public void setCompletedString(String completedString) {
		this.completedString = completedString;
	}
}
